<template>
  <v-container class="main-text">
    <v-card-title>
      <h3>
        {{ courseId }}
      </h3>
    </v-card-title>
    <v-row align="center">
      <v-col cols="12" lg="8" md="8" sm="12">
        <div class="ml-5">Semester: {{ semester }}</div>
        <p class="ml-5">Date: {{ datePosted }}</p>
        <div class="pa-5">
          <p>Description: {{ description }}</p>
        </div>
      </v-col>
      <v-col cols="12" lg="4" md="4" sm="12">
        <v-img width="100" :src="providerId"></v-img>
      </v-col>
      <v-col
        v-for="(note, i) in noteFiles"
        :key="i"
        cols="12"
        lg="4"
        md="4"
        sm="12"
      >
        <v-img v-if="checkFileType(note)" width="100" :src="getLink(note.path)"></v-img>
        <v-img v-else width="100" contain src="@/assets/download_image.png"></v-img>
        {{note.originalname}}
      </v-col>
    </v-row>
  </v-container>
</template>

<script>
export default {
  props: {
    courseId: {
      type: String,
    },
    providerId: {
      type: String,
    },
    noteFiles: [],
    datePosted: {
      type: String,
    },
    description: {
      type: String,
    },
    semester: {
      type: String,
    },
  },
  methods: {
    getLink(link) {
      let currLink = "http://localhost:3000/" + link;
      return currLink;
    },
    checkFileType(note) {
      if(note.mimetype.includes('image')){
        return true
      }
      return false
    }
  },
};
</script>

<style scoped>
.main-text {
  font-family: Times, serif, sans-serif;
}
